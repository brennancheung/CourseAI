/**
 * Pre-computed autoencoder reconstruction data for the bottleneck widget.
 *
 * Each image is stored as a 14x14 grid (196 values, 0-255 integers) representing
 * plausible Fashion-MNIST items and their autoencoder reconstructions at various
 * bottleneck sizes. The pixel values are designed to reflect realistic autoencoder
 * behavior:
 *   - Bottleneck 4:   Very blurry, barely recognizable category shape
 *   - Bottleneck 16:  Rough shape preserved, no fine detail
 *   - Bottleneck 32:  Recognizable item, some detail missing
 *   - Bottleneck 64:  Good reconstruction, minor artifacts
 *   - Bottleneck 128: Near-perfect reconstruction
 *   - Bottleneck 256: Near-perfect reconstruction
 *
 * The degradation pattern reflects compression artifacts (loss of high-frequency
 * detail, intensity flattening, feature merging) rather than Gaussian blur.
 */

export type BottleneckSize = 4 | 16 | 32 | 64 | 128 | 256

